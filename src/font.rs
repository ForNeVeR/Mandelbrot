/// This function returns array 8x8 of bool values, where 1 means black color of
/// pixel and 0 means transparent color of pixel.
pub fn get_glyph(character: char) -> &'static PixelArray {
    match character {
        ' ' => &SPACE,
        '.' => &DOT,
        ':' => &COLON,
        '-' => &MINUS,
        '+' => &PLUS,
        '0' => &ZERO,
        '1' => &ONE,
        '2' => &TWO,
        '3' => &THREE,
        '4' => &FOUR,
        '5' => &FIVE,
        '6' => &SIX,
        '7' => &SEVEN,
        '8' => &EIGHT,
        '9' => &NINE,
        'A' => &A_CAPITAL,
        'C' => &C_CAPITAL,
        'E' => &E_CAPITAL,
        'F' => &F_CAPITAL,
        'L' => &L_CAPITAL,
        'P' => &P_CAPITAL,
        'S' => &S_CAPITAL,
        _ => &UNKNOWN
    }
}