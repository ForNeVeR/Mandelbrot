/* This function returns array 8x8 of bool values, where 1 means black color of
 * pixel and 0 means transparent color of pixel. */
const PixelArray &get_character(char character)
{
    switch(character)
    {
    case ' ':
        return SPACE;
    case ':':
        return COLON;
    case '0':
        return ZERO;
    case '1':
        return ONE;
    case '2':
        return TWO;
    case '3':
        return THREE;
    case '4':
        return FOUR;
    case '5':
        return FIVE;
    case '6':
        return SIX;
    case '7':
        return SEVEN;
    case '8':
        return EIGHT;
    case '9':
        return NINE;
    case 'F':
        return F_CAPITAL;
    case 'P':
        return P_CAPITAL;
    case 'S':
        return S_CAPITAL;
    default:
        return UNKNOWN;
    }
}

